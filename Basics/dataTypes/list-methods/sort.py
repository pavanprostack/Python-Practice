
# sort()
# it is used to sorting the elements in list.
# sorting we can do in two ways. 1. Ascending order, 2. descending order.

# 1. Ascending order
fruits=["apple", "mango", "kiwi", "banana", "orange"]

fruits.sort()
print(fruits)

fruits1=["Apple", "orange", "Mango", "kiwi", "banana"]
fruits1.sort()
print(fruits1)


numb=[1, 2, 3, 4, 5, 6]

numb.sort()
print(numb)

# 2. descending order
fruit=["apple", "mango", "kiwi", "banana", "orange"]

fruit.sort(reverse=True)
print(fruit)

nums=[1, 2, 3, 4, 5, 6]

nums.sort(reverse=True)
print(nums)