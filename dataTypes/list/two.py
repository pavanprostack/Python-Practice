
# How to change or update single element in list.

fruits=["apple", "mango", "banana"]

fruits[1]="kiwi"
print(fruits)


# How to change or update multiple elements in list.

fruit=["apple", "mango", "banana", "grapes"]

fruit[1:3]=["kiwi", "orange"]  # it replaces 1, 2 index places with this.
print(fruit)

