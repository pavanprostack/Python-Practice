
# Tuple inbuilt methods are two 1. index(), 2. count().

# 1. index()  ==> it gives index value of given item.

data=("apple", "mango", "orange")

print(data.index("mango"))

# 2. count()  ==> it gives no.of items present in the tuple.

data1=("apple", "mango", "orange", "apple")

print(data1.count("apple"))