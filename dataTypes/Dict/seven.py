
# keys(), values()

# 1. keys()
# it gives keys of dict.

data={'name': 'pavan', 'sal': 45000, 'gender': 'Male', 'status': 'good'}

fetch=data.keys()
print(fetch)


# 2. keys()
# it gives values of dict.

emp={'name': 'pavan', 'sal': 45000, 'gender': 'Male', 'status': 'good'}

getValues=emp.values()
print(getValues)